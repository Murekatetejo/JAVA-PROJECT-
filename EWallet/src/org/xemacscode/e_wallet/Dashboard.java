    private void AmountAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_AmountAncestorAdded
        // TODO add your handling code here:
         try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/e_wallet", "root","");
            String sql = "select * from wallet";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
               
               String amount = String.valueOf(rs.getString("amount"));
               String tblData[] = {amount}; 
               DefaultTableModel tblModel = (DefaultTableModel)Amount.getModel();
               tblModel.addRow(tblData);
               
               
            }
        }
         catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_AmountAncestorAdded

